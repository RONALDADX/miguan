    def _计算近期表现(self, 价格序列: List[float]) -> Dict:
        """计算股票近期表现"""
        if len(价格序列) < 20:
            return {}
        
        当前价 = 价格序列[-1]
        昨日价 = 价格序列[-2] if len(价格序列) >= 2 else 当前价
        五日价 = 价格序列[-5] if len(价格序列) >= 5 else 当前价
        二十日价 = 价格序列[-20] if len(价格序列) >= 20 else 当前价
        
        return {
            "日涨跌": round(((当前价 - 昨日价) / 昨日价) * 100, 2),
            "五日涨跌": round(((当前价 - 五日价) / 五日价) * 100, 2),
            "二十日涨跌": round(((当前价 - 二十日价) / 二十日价) * 100, 2),
            "当前价格": 当前价,
            "价格区间": f"{min(价格序列[-20:]):.2f}-{max(价格序列[-20:]):.2f}"
        }

