    @staticmethod
    def 计算DMI(最高价: List[float], 最低价: List[float], 收盘价: List[float], 周期=14) -> Optional[Dict]:
        """计算DMI指标（A股常用参数）"""
        if len(收盘价) < 周期 + 1:
            return None
        
        # 计算TR、+DM、-DM
        TR值 = []
        正DM = []
        负DM = []
        
        for i in range(1, len(收盘价)):
            # 真实波幅
            TR = max(
                最高价[i] - 最低价[i],
                abs(最高价[i] - 收盘价[i-1]),
                abs(最低价[i] - 收盘价[i-1])
            )
            TR值.append(TR)
            
            # 方向运动
            上升值 = 最高价[i] - 最高价[i-1]
            下降值 = 最低价[i-1] - 最低价[i]
            
            if 上升值 > 下降值 and 上升值 > 0:
                正DM.append(上升值)
                负DM.append(0)
            elif 下降值 > 上升值 and 下降值 > 0:
                正DM.append(0)
                负DM.append(下降值)
            else:
                正DM.append(0)
                负DM.append(0)
        
        # 平滑处理
        def 平滑(数据, 周期):
            if len(数据) < 周期:
                return None
            平滑值 = [sum(数据[:周期])]
            for i in range(周期, len(数据)):
                平滑值.append(平滑值[-1] - 平滑值[-1]/周期 + 数据[i])
            return 平滑值
        
        TR平滑 = 平滑(TR值, 周期)
        正DM平滑 = 平滑(正DM, 周期)
        负DM平滑 = 平滑(负DM, 周期)
        
        if not (TR平滑 and 正DM平滑 and 负DM平滑):
            return None
        
        # 计算+DI、-DI
        正DI = []
        负DI = []
        for i in range(len(TR平滑)):
            if TR平滑[i] == 0:
                正DI.append(0)
                负DI.append(0)
            else:
                正DI.append((正DM平滑[i] / TR平滑[i]) * 100)
                负DI.append((负DM平滑[i] / TR平滑[i]) * 100)
        
        # 计算DX
        DX值 = []
        for i in range(len(正DI)):
            if 正DI[i] + 负DI[i] == 0:
                DX值.append(0)
            else:
                DX值.append(abs(正DI[i] - 负DI[i]) / (正DI[i] + 负DI[i]) * 100)
        
        # 计算ADX（DX的周期日平均）
        ADX值 = []
        for i in range(周期-1, len(DX值)):
            ADX = sum(DX值[i-周期+1:i+1]) / 周期
            ADX值.append(ADX)
        
        # 获取最新值
        当前正DI = 正DI[-1] if 正DI else 0
        当前负DI = 负DI[-1] if 负DI else 0
        当前ADX = ADX值[-1] if ADX值 else 0
        
        # 获取前一天值用于判断金叉
        前日正DI = 正DI[-2] if len(正DI) >= 2 else 当前正DI
        前日ADX = ADX值[-2] if len(ADX值) >= 2 else 当前ADX
        
        # 判断DI1上穿ADX
        DI1上穿ADX = 前日正DI <= 前日ADX and 当前正DI > 当前ADX
        
        return {
            "DI+": round(当前正DI, 2),
            "DI-": round(当前负DI, 2),
            "ADX": round(当前ADX, 2),
            "DI1上穿ADX": DI1上穿ADX,
            "趋势强度": "强" if 当前ADX > 25 else "中等" if 当前ADX > 20 else "弱",
            "多头占优": 当前正DI > 当前负DI
        }
    
    @staticmethod
    def 计算RSI(价格: List[float], 短周期=6, 长周期=12) -> Optional[Dict]:
        """计算RSI指标（A股常用6/12周期）"""
        if len(价格) < 长周期 + 1:
            return None
        
        def 计算单周期RSI(数据, 周期):
            涨幅 = []
            跌幅 = []
            for i in range(1, len(数据)):
                变化 = 数据[i] - 数据[i-1]
                if 变化 > 0:
                    涨幅.append(变化)
                    跌幅.append(0)
                else:
                    涨幅.append(0)
                    跌幅.append(abs(变化))
            
            if len(涨幅) < 周期:
                return None
            
            平均涨幅 = sum(涨幅[-周期:]) / 周期
            平均跌幅 = sum(跌幅[-周期:]) / 周期 if 跌幅 else 0
            
            if 平均跌幅 == 0:
                return 100
            else:
                RS = 平均涨幅 / 平均跌幅
                return 100 - (100 / (1 + RS))
        
        # 计算RSI6和RSI12
        RSI6 = 计算单周期RSI(价格, 短周期)
        RSI12 = 计算单周期RSI(价格, 长周期)
        
        if RSI6 is None or RSI12 is None:
            return None
        
        # 判断金叉（RSI6上穿RSI12）
        if len(价格) >= 短周期 + 2:
            前日价格 = 价格[:-1]
            前日RSI6 = 计算单周期RSI(前日价格, 短周期)
            前日RSI12 = 计算单周期RSI(前日价格, 长周期)
        else:
            前日RSI6 = RSI6
            前日RSI12 = RSI12
        
        金叉 = 前日RSI6 <= 前日RSI12 and RSI6 > RSI12
        
        return {
            "RSI6": round(RSI6, 2),
            "RSI12": round(RSI12, 2),
            "金叉": 金叉,
            "状态": "超买" if RSI6 > 80 else "强势" if RSI6 > 50 else "弱势" if RSI6 > 20 else "超卖"
        }
    
    @staticmethod
    def 计算布林带(价格: List[float], 周期=20, 标准差倍数=2) -> Optional[Dict]:
        """计算布林带指标"""
        if len(价格) < 周期:
            return None
        
        # 中轨 = N日移动平均线
        中轨 = sum(价格[-周期:]) / 周期
        
        # 计算标准差
        方差 = sum((x - 中轨) ** 2 for x in 价格[-周期:]) / 周期
        标准差 = math.sqrt(方差)
        
        # 上轨和下轨
        上轨 = 中轨 + 标准差倍数 * 标准差
        下轨 = 中轨 - 标准差倍数 * 标准差
        
        # 当前价格位置
        当前价格 = 价格[-1]
        突破中轨 = 当前价格 > 中轨
        
        # 布林带宽度（波动率指标）
        带宽 = ((上轨 - 下轨) / 中轨) * 100
        
        # 判断三轨趋势
        if len(价格) >= 周期 * 2:
            前日中轨 = sum(价格[-周期*2:-周期]) / 周期
            三轨上行 = 中轨 > 前日中轨
        else:
            三轨上行 = False
        
        return {
            "上轨": round(上轨, 2),
            "中轨": round(中轨, 2),
            "下轨": round(下轨, 2),
            "当前价格": round(当前价格, 2),
            "突破中轨": 突破中轨,
            "带宽百分比": round(带宽, 2),
            "三轨上行": 三轨上行,
            "位置": "上轨附近" if 当前价格 > 上轨 * 0.9 else "中轨上方" if 突破中轨 else "中轨下方"
        }
    
    @staticmethod
    def 计算换手率(成交量: List[int], 流通股本: float, 天数=20) -> Optional[float]:
        """计算换手率"""
        if len(成交量) < 天数 or 流通股本 <= 0:
            return None
        
        # 平均成交量
        平均成交量 = sum(成交量[-天数:]) / 天数
        
        # 换手率 = 成交量 / 流通股本 * 100%
        换手率 = (平均成交量 / 流通股本) * 100
        
        return round(换手率, 2)
    
